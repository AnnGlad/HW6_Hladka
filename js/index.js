function editBtnListener() {
  edit = true;
  let editBtn = document.querySelectorAll(".btn-edit");
  editBtn.forEach(function (el) {
    el.addEventListener("click", function (event) {
      event.stopPropagation();
      this.classList.toggle("active");
      let parent = this.parentNode;
      parent.classList.toggle("edit");

      let editIngr = parent.getElementsByClassName("ingr");
      let pizzaName = parent.querySelector(".prod-name > a").innerText;
      let prodPrice = parent.querySelector(".prod-price");
      let prodCal = parent.querySelector(".prod-kkal");

      let spans = this.parentNode.querySelectorAll(".ingr");

      spans.forEach(function (el) {

        el.addEventListener("click", function (e) {
          e.stopPropagation();
          el.classList.toggle("linethrough");

          recalc(editIngr, pizzaName, prodPrice, prodCal);

        })

      });


    });
  })
}


function recalc(arr, name, prodprice, prodCal) {

  let price = 0;
  let cal = 0;

  for (let i = 0; i < arr.length; i++) {
    if (!(arr[i].className.includes("linethrough"))) {
      ingrsPriceAndCal.forEach(function (val, key) {
        let pizIngrHTML = arr[i].innerText.toLocaleLowerCase();
        if (pizIngrHTML == key) {
          let pizIngrPrice = val.price;
          let pizCal = val.cal;
          price += pizIngrPrice;
          cal += pizCal;
        }
      });
    }
  }
  if (name.includes("метровая") || name.includes("XXL")) {
    price *= 2;
    cal *= 2;
  }

  prodprice.innerText = price + " грн";
  prodCal.textContent = cal + " кал."




};

