function calcPriceAndCal() {
  for (let i = 0; i < pizzaArr.length; i++) {
    let price = 0;
    let cal = 0;
    for (let j = 0; j < pizzaArr[i].ingredients.length; j++) {

      let pizIngr = pizzaArr[i].ingredients[j].toLocaleLowerCase();

      ingrsPriceAndCal.forEach(function (val, key) {
        let pizIngrPrice = val.price;
        let pizCal = val.cal;
        if (pizIngr == key) {

          price += pizIngrPrice;
          cal += pizCal;
        }

      })

    }


    if (pizzaArr[i].name.includes("метровая") || pizzaArr[i].name.includes("XXL")) {
      price *= 2;
      cal *= 2;

    }
    pizzaPrices.push(price);
    pizzaCals.push(cal);
  }
}

calcPriceAndCal();


